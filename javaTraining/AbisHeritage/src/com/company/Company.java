package com.company;

public class Company {

    // fields
    /**
     * this are fields
     */
    private String name;

    // constructors
    /**
     * this are constructors
     * @param name
     */

    public Company(String name) {
        this.name = name;
    }

    // getters and setters

    /**
     * this are getters and setters
     * @return
     */
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
